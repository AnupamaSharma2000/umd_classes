"""
Dual LLM judge (Claude + GPT-4o) for scoring code quality and explanation quality.
Reports inter-rater agreement (Cohen's kappa) across evaluation dimensions.

Usage:
    from src.llm_judge import judge_code_quality, judge_explainability
"""
import os
import sys
import json
import re
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent.parent))
from src.utils import log


CODE_QUALITY_PROMPT = """You are evaluating Python code generated by a data science AI agent.

TASK DESCRIPTION:
{task_description}

GENERATED CODE:
```python
{generated_code}
```

Score EACH dimension from 1 to 10 (integers only):
1. READABILITY: Clear variable names, logical structure, consistent formatting
2. MODULARITY: Use of functions/classes, separation of concerns, no code duplication
3. COMMENTS: Docstrings, inline comments explaining logic, section headers
4. RE_RUNNABILITY: Can this code run independently? No hardcoded paths, all imports included, seeds set
5. BEST_PRACTICES: Error handling, type hints, logging, reproducibility (random seeds)

Respond with ONLY a JSON object, no other text:
{{"readability": N, "modularity": N, "comments": N, "re_runnability": N, "best_practices": N, "overall": N}}

The "overall" score should be your holistic assessment (1-10), not necessarily the average."""


EXPLAINABILITY_PROMPT = """You are evaluating the quality of a data science explanation produced by an AI agent.

TASK: The agent was asked to explain a machine learning model's predictions.

AGENT'S EXPLANATION:
{explanation_text}

AGENT'S CODE (if relevant):
```python
{generated_code}
```

Score EACH dimension from 1 to 10 (integers only):
1. CLARITY: Is the explanation understandable to a non-technical stakeholder?
2. CORRECTNESS: Are the claims factually accurate and consistent with the code?
3. COMPLETENESS: Does it cover the key features, their direction of impact, and relative importance?
4. ACTIONABILITY: Could someone use this explanation to make a decision?

Respond with ONLY a JSON object, no other text:
{{"clarity": N, "correctness": N, "completeness": N, "actionability": N, "overall": N}}"""


def _call_llm(provider: str, prompt: str) -> tuple[str | None, str | None]:
    """Call an LLM by provider name and return (text, error). Supports 'claude' and 'gpt4o'."""
    if provider == "claude":
        api_key = os.environ.get("ANTHROPIC_API_KEY")
        if not api_key:
            return None, "ANTHROPIC_API_KEY not set"
        try:
            import anthropic
            client = anthropic.Anthropic(api_key=api_key)
            response = client.messages.create(
                model="claude-sonnet-4-6",
                max_tokens=500,
                temperature=0,
                messages=[{"role": "user", "content": prompt}],
            )
            return response.content[0].text, None
        except Exception as e:
            return None, str(e)

    if provider == "gpt4o":
        api_key = os.environ.get("OPENAI_API_KEY")
        if not api_key:
            return None, "OPENAI_API_KEY not set"
        try:
            import openai
            client = openai.OpenAI(api_key=api_key)
            response = client.chat.completions.create(
                model="gpt-4o",
                messages=[{"role": "user", "content": prompt}],
                temperature=0,
                max_tokens=500,
            )
            return response.choices[0].message.content, None
        except Exception as e:
            return None, str(e)

    return None, f"Unknown provider: {provider}"


def _parse_json_response(text):
    """Extract a JSON object from an LLM response, stripping markdown fences if present."""
    if not text:
        return None
    cleaned = re.sub(r"```json\s*", "", text)
    cleaned = re.sub(r"```\s*", "", cleaned).strip()
    try:
        return json.loads(cleaned)
    except json.JSONDecodeError:
        pass
    # Greedy slice from first { to last } to handle nested objects
    start, end = cleaned.find("{"), cleaned.rfind("}")
    if start != -1 and end > start:
        try:
            return json.loads(cleaned[start : end + 1])
        except json.JSONDecodeError:
            pass
    return None


def _cohens_kappa(scores_a, scores_b):
    """
    Compute Cohen's kappa between two sets of ordinal scores.
    Scores are bucketed into low (1-4), medium (5-7), high (8-10) before computing.
    Falls back to simple agreement rate if sklearn is unavailable — note this is NOT
    true kappa; install scikit-learn for the correct metric.
    """
    if not scores_a or not scores_b or len(scores_a) != len(scores_b):
        return None

    def bucket(s):
        if s <= 4:
            return 0
        elif s <= 7:
            return 1
        return 2

    a = [bucket(s) for s in scores_a]
    b = [bucket(s) for s in scores_b]

    try:
        from sklearn.metrics import cohen_kappa_score
        return round(cohen_kappa_score(a, b), 3)
    except ImportError:
        n = len(a)
        return round(sum(x == y for x, y in zip(a, b)) / n, 3) if n > 0 else None


def _run_judges(prompt: str, dimensions: list[str], use_both_judges: bool) -> dict:
    """
    Call one or both LLM judges, parse their responses, compute combined score and kappa.
    Returns a dict with claude_scores, gpt4o_scores, combined_overall, cohens_kappa.
    """
    claude_text, claude_err = _call_llm("claude", prompt)
    claude_scores = _parse_json_response(claude_text) if not claude_err else None

    gpt_scores = None
    if use_both_judges:
        gpt_text, gpt_err = _call_llm("gpt4o", prompt)
        gpt_scores = _parse_json_response(gpt_text) if not gpt_err else None

    combined_overall = None
    kappa = None

    if claude_scores and gpt_scores:
        combined_overall = round(
            (claude_scores.get("overall", 5) + gpt_scores.get("overall", 5)) / 2, 1
        )
        kappa = _cohens_kappa(
            [claude_scores.get(k, 5) for k in dimensions],
            [gpt_scores.get(k, 5) for k in dimensions],
        )
    elif claude_scores:
        combined_overall = claude_scores.get("overall")
    elif gpt_scores:
        combined_overall = gpt_scores.get("overall")

    return {
        "claude_scores": claude_scores,
        "gpt4o_scores": gpt_scores,
        "combined_overall": combined_overall,
        "cohens_kappa": kappa,
    }


def judge_code_quality(generated_code, task_description, use_both_judges=True):
    """Score code quality using two LLM judges for inter-rater reliability."""
    if not generated_code or len(generated_code.strip()) < 20:
        return {
            "claude_scores": None,
            "gpt4o_scores": None,
            "combined_overall": 0.0,
            "cohens_kappa": None,
            "note": "No code to evaluate",
        }

    prompt = CODE_QUALITY_PROMPT.format(
        task_description=task_description[:500],
        generated_code=generated_code[:3000],
    )
    dimensions = ["readability", "modularity", "comments", "re_runnability", "best_practices"]
    return _run_judges(prompt, dimensions, use_both_judges)


def judge_explainability(explanation_text, generated_code=None, use_both_judges=True):
    """Score explanation quality using two LLM judges for inter-rater reliability."""
    if not explanation_text or len(explanation_text.strip()) < 20:
        return {
            "claude_scores": None,
            "gpt4o_scores": None,
            "combined_overall": 0.0,
            "cohens_kappa": None,
            "note": "No explanation to evaluate",
        }

    prompt = EXPLAINABILITY_PROMPT.format(
        explanation_text=explanation_text[:2000],
        generated_code=(generated_code or "Not available")[:2000],
    )
    dimensions = ["clarity", "correctness", "completeness", "actionability"]
    return _run_judges(prompt, dimensions, use_both_judges)


def judge_all_results(results_dir=None):
    """
    Walk results/ and run LLM-as-Judge on every result.json that has generated code.
    Updates each scorecard.json in-place with D2_llm_judge and D3_llm_judge scores.
    """
    from src.utils import RESULTS_DIR, load_json, save_json, load_tasks_config

    results_dir = Path(results_dir) if results_dir else RESULTS_DIR
    tasks = load_tasks_config()
    updated = 0

    for result_path in results_dir.rglob("result.json"):
        result = load_json(result_path)
        scorecard_path = result_path.parent / "scorecard.json"
        code = result.get("generated_code")
        # Derive task_id from path because result.json historically holds
        # "?" (wrappers default that way). Path layout is
        # results/<agent>/<task_id>/run_<n>/result.json.
        parts = result_path.relative_to(results_dir).parts
        task_id = parts[1] if len(parts) >= 2 else result.get("task_id", "")
        task_desc = tasks.get(task_id, {}).get("prompt", "")[:500]

        if not code or len(code.strip()) <= 20:
            continue

        log(f"[llm_judge] Judging: {result.get('agent')}/{task_id}/run_{result.get('run_id')}")

        cq = judge_code_quality(code, task_desc, use_both_judges=True)
        eq = judge_explainability(result.get("raw_output", ""), code, use_both_judges=True)

        if scorecard_path.exists():
            sc = load_json(scorecard_path)
            sc.setdefault("D2_code_quality", {})["llm_judge_score"] = cq.get("combined_overall")
            sc.setdefault("D2_code_quality", {})["llm_judge_detail"] = cq
            sc.setdefault("D3_explainability", {})["llm_judge_score"] = eq.get("combined_overall")
            sc.setdefault("D3_explainability", {})["llm_judge_detail"] = eq
            save_json(sc, scorecard_path)
            updated += 1

    log(f"[llm_judge] Updated {updated} scorecards.")
    return updated


if __name__ == "__main__":
    judge_all_results()