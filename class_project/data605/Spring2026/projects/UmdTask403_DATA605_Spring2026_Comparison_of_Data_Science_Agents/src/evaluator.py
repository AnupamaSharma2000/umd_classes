"""
Object: Multi-Dimensional Evaluator
Scores agent results on D1-D6.

D1: Accuracy/Quality    (automated, task-specific)
D2: Code Quality        (pylint + LLM-as-Judge)
D3: Explainability      (SHAP detection + explanation quality)
D4: Speed               (wall-clock seconds)
D5: Cost                (API pricing)
D6: Robustness          (variance across runs + adversarial degradation)
"""
import os
import json
import subprocess
import tempfile
import numpy as np

from src.utils import log, save_json
from src.cost_tracker import calculate_cost


# =============================================================================
# D1: Accuracy / Quality
# =============================================================================

def score_accuracy(task_type, y_true=None, y_pred=None, y_prob=None, rubric_score=None):
    """
    D1: Task-specific accuracy metrics.
    For classification: F1, accuracy, AUC-ROC
    For regression: RMSE, MAE, R²
    For rubric-based tasks (EDA, descriptive, prescriptive): pass rubric_score directly
    """
    if task_type in ("descriptive", "eda", "prescriptive", "explainability", "end_to_end"):
        return {"type": "rubric", "rubric_score": rubric_score, "note": "Scored by LLM-as-Judge"}

    if y_true is None or y_pred is None:
        return {"type": task_type, "error": "No predictions provided"}

    from sklearn.metrics import (
        f1_score, accuracy_score, roc_auc_score,
        mean_squared_error, mean_absolute_error, r2_score,
    )

    y_true = np.array(y_true)
    y_pred = np.array(y_pred)

    if task_type == "classification":
        result = {
            "type": "classification",
            "f1_weighted": round(f1_score(y_true, y_pred, average="weighted", zero_division=0), 4),
            "accuracy": round(accuracy_score(y_true, y_pred), 4),
        }
        if y_prob is not None:
            try:
                y_prob = np.array(y_prob)
                if len(np.unique(y_true)) == 2:
                    result["auc_roc"] = round(roc_auc_score(y_true, y_prob), 4)
                else:
                    result["auc_roc"] = round(roc_auc_score(y_true, y_prob, multi_class="ovr", average="weighted"), 4)
            except Exception as e:
                result["auc_roc"] = None
                result["auc_error"] = str(e)
        return result

    elif task_type in ("regression", "forecasting"):
        return {
            "type": "regression",
            "rmse": round(float(np.sqrt(mean_squared_error(y_true, y_pred))), 4),
            "mae": round(float(mean_absolute_error(y_true, y_pred)), 4),
            "r2": round(float(r2_score(y_true, y_pred)), 4),
        }

    return {"type": task_type, "error": f"Unknown task_type: {task_type}"}


# =============================================================================
# D2: Code Quality
# =============================================================================

def score_code_quality(code_string):
    """
    D2: Code quality via pylint (automated half).
    Returns pylint-based score on 0-10 scale.
    LLM-as-Judge score is added separately via llm_judge.py.
    """
    if not code_string or not isinstance(code_string, str) or len(code_string.strip()) < 10:
        return {
            "pylint_score": 0.0,
            "llm_judge_score": None,
            "combined": 0.0,
            "note": "No code generated by agent",
        }

    # Write code to temp file and run pylint
    try:
        with tempfile.NamedTemporaryFile(mode="w", suffix=".py", delete=False) as f:
            f.write(code_string)
            tmp_path = f.name

        result = subprocess.run(
            ["pylint", tmp_path, "--output-format=json", "--disable=C0114,C0115,C0116,C0103,R,C0301"],
            capture_output=True, text=True, timeout=30,
        )

        if result.stdout.strip():
            messages = json.loads(result.stdout)
            n_errors = sum(1 for m in messages if m.get("type") == "error")
            n_warnings = sum(1 for m in messages if m.get("type") == "warning")
            n_conventions = sum(1 for m in messages if m.get("type") == "convention")
            # Score: start at 10, deduct for issues
            pylint_score = max(0.0, 10.0 - n_errors * 3.0 - n_warnings * 1.0 - n_conventions * 0.3)
        else:
            pylint_score = 8.0  # No issues found = good

    except subprocess.TimeoutExpired:
        pylint_score = 5.0
        n_errors, n_warnings = -1, -1
    except FileNotFoundError:
        log.warning("pylint not installed. Skipping code quality scoring.")
        pylint_score = None
        n_errors, n_warnings = -1, -1
    except Exception as e:
        pylint_score = 0.0
        n_errors, n_warnings = -1, -1
        log.warning(f"pylint error: {e}")
    finally:
        try:
            os.unlink(tmp_path)
        except Exception:
            pass

    return {
        "pylint_score": round(pylint_score, 2) if pylint_score is not None else None,
        "llm_judge_score": None,  # Filled by llm_judge.py later
        "combined": None,  # (pylint + llm_judge) / 2
    }


# =============================================================================
# D3: Explainability
# =============================================================================

def score_explainability(result_dict):
    """
    D3: Did the agent produce SHAP / feature importance?
    Automated detection + LLM-as-Judge for explanation quality.
    """
    code = str(result_dict.get("generated_code", "") or "")
    raw = str(result_dict.get("raw_output", "") or "")
    combined_text = (code + " " + raw).lower()

    has_shap = "shap" in combined_text and ("summary_plot" in combined_text or "shap.plots" in combined_text or "explainer" in combined_text)
    has_feature_importance = "feature_importance" in combined_text or "feature_importances_" in combined_text
    has_explanation_text = len([line for line in raw.split("\n") if len(line) > 80]) >= 3  # ≥3 long explanation lines

    auto_score = 0.0
    if has_shap:
        auto_score += 4.0
    if has_feature_importance:
        auto_score += 3.0
    if has_explanation_text:
        auto_score += 3.0

    return {
        "shap_generated": has_shap,
        "feature_importance_generated": has_feature_importance,
        "has_explanation_text": has_explanation_text,
        "auto_score": min(auto_score, 10.0),
        "llm_judge_score": None,  # Filled later
    }


# =============================================================================
# D4: Speed
# =============================================================================

def score_speed(wall_clock_sec):
    """D4: Wall-clock time in seconds."""
    return {"wall_clock_sec": round(wall_clock_sec, 3)}


# =============================================================================
# D5: Cost
# =============================================================================

def score_cost(agent_id, input_tokens=0, output_tokens=0):
    """D5: API cost in USD."""
    cost = calculate_cost(agent_id, input_tokens, output_tokens)
    return {
        "api_cost_usd": cost,
        "input_tokens": input_tokens,
        "output_tokens": output_tokens,
        "tokens_total": input_tokens + output_tokens,
    }


# =============================================================================
# D6: Robustness
# =============================================================================

def score_robustness(run_scores):
    """
    D6: Robustness from multiple runs.
    run_scores: list of primary metric values from repeated runs.
    """
    if not run_scores or len(run_scores) < 2:
        return {"mean": None, "std": None, "cv": None, "n_runs": len(run_scores) if run_scores else 0}

    arr = np.array([s for s in run_scores if s is not None])
    if len(arr) < 2:
        return {"mean": None, "std": None, "cv": None, "n_runs": len(arr)}

    mean = float(np.mean(arr))
    std = float(np.std(arr))
    cv = float(std / mean) if mean != 0 else None

    return {
        "mean": round(mean, 4),
        "std": round(std, 4),
        "cv": round(cv, 4) if cv is not None else None,
        "n_runs": len(arr),
    }


# =============================================================================
# Full Evaluator
# =============================================================================

def evaluate_result(result, task_config, all_run_scores=None):
    """
    Score a single (agent, task, run) result on all 6 dimensions.

    Args:
        result: dict from agent wrapper (standard result format)
        task_config: dict from tasks.yaml for this task
        all_run_scores: list of primary metric values from all runs (for D6)

    Returns:
        scorecard: dict with D1-D6 scores
    """
    task_type = task_config.get("task_type", "classification")
    agent_id = result.get("agent", "unknown")

    scorecard = {
        "agent": agent_id,
        "task_id": result.get("task_id"),
        "run_id": result.get("run_id"),
        "timestamp": result.get("timestamp"),
        "D1_accuracy": score_accuracy(
            task_type=task_type,
            y_true=result.get("y_true"),
            y_pred=result.get("predictions"),
            y_prob=result.get("y_prob"),
            rubric_score=result.get("rubric_score"),
        ),
        "D2_code_quality": score_code_quality(result.get("generated_code")),
        "D3_explainability": score_explainability(result),
        "D4_speed": score_speed(result.get("wall_clock_sec", 0)),
        "D5_cost": score_cost(
            agent_id=agent_id,
            input_tokens=result.get("input_tokens", 0),
            output_tokens=result.get("output_tokens", 0),
        ),
        "D6_robustness": score_robustness(all_run_scores) if all_run_scores else None,
    }

    return scorecard
